    {
        try {
            $order = new Order();
            $order = $order->create($request->only(['name', 'total']));
            $order->saveSaucers($request->saucers);
            return $this->successResponse(compact('order'), 200);
        } catch (\Exception $e) {
            return $this->errorResponse($e->getMessage(), 500);
        }
    }

